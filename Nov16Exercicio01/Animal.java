package br.com.generation.Nov16Exercicio01;

public class Animal {
	
	//atributos
	private String nome;
	private int idade;
	
	//encapsulando
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public int getIdade() {
		return idade;
	}
	public void setIdade(int idade) {
		this.idade = idade;
	}

}
